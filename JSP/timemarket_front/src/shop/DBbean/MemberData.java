package shop.DBbean;

public class MemberData {
	private String userid;
	private String userpw;
	private String userphone;
	private String username;
	private String useremail;
	
	
	public String getUserid() {
		return userid;
	}
	public String getUserpw() {
		return userpw;
	}
	public String getUserphone() {
		return userphone;
	}
	public String getUsername() {
		return username;
	}
	public String getUseremail() {
		return useremail;
	}
	public String setUserid(String userid) {
		return this.userid = userid;
	}
	public String setUserpw(String userpw) {
		return this.userpw = userpw;
	}
	public String setUserphone(String userphone) {
		return this.userphone = userphone;
	}
	public String setUsername(String username) {
		return this.username = username;
	}
	public String setUseremail(String useremail) {
		return this.useremail = useremail;
	}
	
	
}
